#include "Clock.h"
#include <coredecls.h> 

Clock c;

void setNTPtime() {
  c.checkNTP();
}

void setup() {
  settimeofday_cb(setNTPtime);
  c.begin();
}


void loop() {
  c.loop();
  yield();
}
